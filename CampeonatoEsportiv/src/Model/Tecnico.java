package Model;

public class Tecnico {
    private Pessoa pessoa;
    private Times time;
    public Tecnico() {
    }
    public Pessoa getPessoa() {
        return pessoa;
    }
    public void setPessoa(Pessoa pessoa) {
        this.pessoa = pessoa;
    }
    public Times getTime() {
        return time;
    }
    public void setTime(Times time) {
        this.time = time;
    }


}
