package Control.Model;

public class Vacina {
    private String nome;
    private String codigo;
    private String validade;
    private int formaAdiministracao;
    public Vacina() {
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getCodigo() {
        return codigo;
    }
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
    public String getValidade() {
        return validade;
    }
    public void setValidade(String validade) {
        this.validade = validade;
    }
    public int getFormaAdiministracao() {
        return formaAdiministracao;
    }
    public void setFormaAdiministracao(int formaAdiministracao) {
        this.formaAdiministracao = formaAdiministracao;
    }

    

}
